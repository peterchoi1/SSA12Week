package com.tiy.ssa.weekone.assignmentone;

public class Power {

	
	public int powerOne(int a){
		return a;
	}
	
	public int powerTwo(int a){
		return a * a;
	}
	
	public int powerThree(int a){
		return a * a * a;
	}
	
	public int powerFour(int a){
		
		return a * a * a * a;
	}
	
	public int powerFive(int a){
		
		return a * a * a * a * a;
	}
	
	
		
	
	
	
	
}
